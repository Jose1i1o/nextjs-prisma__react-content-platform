import { Nunito, Onest } from "next/font/google";
// import type { Metadata } from "next";
import "./globals.css";
import Navbar from "./components/Navbar/Navbar";
import ClientOnly from "./components/ClientOnly";
import RegisterModal from "./components/Modal/RegisterModal";
import ToasterProvider from "./components/shared/Toasters/ToasterProvider";
import LoginModal from "./components/Modal/LoginModal";
import getCurrentUser from "./actions/getCurrentUser";
import { Providers } from "./providers";
import Head from 'next/head';
import { theme } from "@/theme/theme";

type Metadata = {
	title: string;
	description: string;
};

const metadata: Metadata = {
	title: "AIT Documentation",
	description: "Generated by create next app",
};

const font = Onest({
	subsets: ["latin"],
});

const font2 = Nunito({
	subsets: ["latin"],
});


const customBackground = {
	backgroundColor: theme.colors.black
};


export default async function RootLayout({
	children,
}: {
	children: React.ReactNode;
}) {
	const currentUser = await getCurrentUser();

	return (
		<html lang="en">
			<Head>
				<title>{metadata.title}</title>
				<meta name="description" content={metadata.description} />
			</Head>
			<body style={customBackground} className={ font.className || font2.className }>
				<Providers>
					<ClientOnly>
						<ToasterProvider />
						<RegisterModal />
						<LoginModal />
						<Navbar currentuser={currentUser} />
					</ClientOnly>
				</Providers>
				<div className="">{children}</div>
			</body>
		</html>
	);
}
